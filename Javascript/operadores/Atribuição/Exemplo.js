const valor1 = 45;
let valor2 = 7;

// valor2 = valor2 + valor1
valor2 += valor1; 
console.log(valor2);

// valor2 = valor2 - valor1
valor2 -= valor1; 
console.log(valor2);

// valor2 = valor2 * valor1
valor2 *= valor1;
console.log(valor2);

// valor2 = valor2 / valor1
valor2 /= valor1;
console.log(valor2);

// valor2 = valor2 % valor1
valor2 %= valor1;
console.log(valor2);
